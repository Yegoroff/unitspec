from unitspec import SpecTestCase


class ReturnContextTestCase(SpecTestCase):

    '''
        def test_string_addition(self):

            def establish():
                value = "A"
                return value

            def act():
                value += "B"

            def it_should_be_ab():
                self.assertEqual(value, "AB")

            def it_should_be_abc():
                self.assertEqual(value, "AB")
    '''

